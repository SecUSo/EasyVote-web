<p>Wählen Sie links "Wahlzettel anzeigen" aus, um daraufhin eine Stimme für eine Partei zu vergeben.</p>
<br></br>
<p>Kreuze können durch abermaliges Ankreuzen der Partei gel&ouml;scht oder durch Wählen einer anderen Partei versetzt werden.</p>
<br></br>
<p>Um Ihren Stimmzettel als "ungültig" zu kennzeichnen, wählen Sie in der linken Spalte "Ungültig wählen" aus.</p>
<br></br>
<p>Um Ihre Stimme im Anschluss zu drucken oder hochzuladen, drücken Sie auf "Stimme ansehen". </p>