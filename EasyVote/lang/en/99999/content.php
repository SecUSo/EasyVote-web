<p>Select "Show ballot card" on the left hand side to show the ballot card. You are then able to cast your first and second vote to a candidate and a party.</p>
<br></br>
<p>To reverse a vote cast uncheck it. You are also able to change your vote by selecting another party or a candidate respectively.</p>
<br></br>
<p>Select "Spoil ballot" in the left cloumn to spoil your ballot card.</p>
<br></br>
<p>If you want to print or upload your ballot card click on "View ballot card". </p>